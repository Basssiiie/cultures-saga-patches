from utils.executable import Executable
from utils.patch import Patch


class MultiplayerTransferSize(Patch):
	def apply(self, exe: Executable) -> None:
		# Patch size of packets for transferring saves/custom maps in multiplayer matches
		size = 0x5000 # 32 KB

		# Increase the stack and push new size
		exe.verify_and_replace(0xDFBD3, [ 0xb8, 0x68, 0x11, 0x00, 0x00 ], f"MOV EAX, 0x{size + 0x168:X}")
		exe.verify_and_replace(0xDFD87, [ 0x68, 0x00, 0x08, 0x00, 0x00 ], f"PUSH 0x{size:X}")

		# Shift writing message parameters
		base_pointer = 0x100_000_000 - size
		exe.verify_and_replace(0xDFD8D, [ 0x8d, 0x85, 0x98, 0xee, 0xff, 0xff ], f"LEA EAX, [EBP + 0x{base_pointer - 0x150:X}]")
		exe.verify_and_replace(0xDFDA6, [ 0xc6, 0x85, 0x98, 0xf6, 0xff, 0xff, 0x0f ], f"MOV byte ptr [EBP + 0x{base_pointer - 0x168:X}], 0xF")
		exe.verify_and_replace(0xDFDAF, [ 0x8d, 0xbd, 0x9c, 0xf6, 0xff, 0xff ], f"LEA EDI, [EBP + 0x{base_pointer - 0x164:X}]")
		exe.verify_and_replace(0xDFDC0, [ 0x89, 0x85, 0xac, 0xf6, 0xff, 0xff ], f"MOV dword ptr [EBP + 0x{base_pointer - 0x154:X}], EAX")

		# Nop out mem-copy to duplicate buffer
		exe.verify_and_replace(0xDFDD4, [ 0xe8, 0x77, 0x5b, 0x00, 0x00 ], [ 0x90 ] * 5)

		# Shift address and update size for sending message
		exe.verify_and_replace(0xDFDDF, [ 0x8d, 0x85, 0x98, 0xf6, 0xff, 0xff ], f"LEA EAX,[EBP + 0x{base_pointer - 0x168:X}]")
		exe.verify_and_replace(0xDCFDA, [ 0xc7, 0x45, 0xf8, 0x18, 0x08, 0x00, 0x00 ], f"MOV dword ptr [EBP + -0x8], 0x{size + 0x18:X}")
